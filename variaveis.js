/*
* var - pode ser recriada e alterada o valor
* sempre que quiser
*/
var nome = "jose";
nome = "carlos";
var nome = "marcos";
console.log(nome);

/**
 * let - não pode ser recriada ,mas pode ser alterada
 * sempre que quiser
 */
let idade = 25;
idade = 23;
console.log(idade);

/**
 * const - não pode ser criada nem alterada
 */
const email = "example@email.com";
console.log(email);
